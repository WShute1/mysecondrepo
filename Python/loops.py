#For loop to print numbers from 1 to 5
for any_number in range(1,6):
    print(any_number) #This prints each number within the above specified range excluding the upper limit value.

#For loop to print each favorite color
favorite_colors=["blue","green","red","yellow","orange"]
for color in favorite_colors:
    print(color)

#While loop to print numbers from 10 down to 1
n=10
while n>0:
    print(n)
    n-= 1  #Each time n>0 is achieved, lines 13 and 14 are executed. Line 14 subtracts 1 from the value of n in line 11.